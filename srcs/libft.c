#include "../philo.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

int	str_is_digit(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		i++;
	}
	return (1);
}

int	ft_atoi(const char *str)
{
	int			sign;
	long int	number;
	long int	number_cmp;
	size_t		i;

	i = 0;
	number = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			sign = -1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		number_cmp = (number * 10) + (str[i++] - 48);
		if (number > number_cmp && sign > 0)
			return (-1);
		if (number > number_cmp && sign < 0)
			return (0);
		number = number_cmp;
	}
	return (number * sign);
}
